# COMPARISON OPERATORS
# Output will be in boolean form.


print(3 > 2)

print(3 < 2)

print(3 <= 2)

print(3 >= 2)

print(3 == 2) # '==' will check whether the two values are equal or not.

print(3 == 3)

print(3 != 3) # '!=' represents not equal. this will reverse the output i.e false will be true and true will be false.

print(3 != 2) # Here, the output will be True because of the not equal operator.
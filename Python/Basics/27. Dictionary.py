# DICTIONARY
# It is used to store 'key' and 'value' in pairs.

marks = {"english" : 95, "chemistry" : 98 }

# To access the information

print(marks["chemistry"])

# To add new marks:

marks["physics"] = 97;
print(marks)            # after running the code, dictionary will be updated.



# to change the marks of physics:

marks["physics"] = 99;
print(marks)